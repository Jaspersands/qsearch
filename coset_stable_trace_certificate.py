CycleMonomial: TypeAlias = tuple[int, ...]
FallingCharacterTerms: TypeAlias = tuple[tuple[CycleMonomial, Fraction], ...]

XI_CHARACTER_TERMS: FallingCharacterTerms = (
    ((1,), Fraction(1)),
    ((1, 1), Fraction(-1)),
    ((1, 1, 1), Fraction(1, 3)),
    ((3,), Fraction(-1)),
)
W_CHARACTER_TERMS: FallingCharacterTerms = (
    ((1, 1), Fraction(1, 2)),
    ((1,), Fraction(-1)),
    ((2,), Fraction(1)),
)


@dataclass(frozen=True)
class StableTraceCertificate:
    created_at: str
    theorem: dict[str, object]
    character_polynomial_certificate: dict[str, object]
    equality_pattern_certificate: dict[str, object]
    stable_symbolic_certificate: dict[str, object]
    endpoint_certificate: dict[str, object]
    headline_metrics: dict[str, int | float]
    claim_gate: dict[str, bool | str]
    status: str
    summary: str
    falsifiers_triggered: list[str]


def _falling(value: sp.Expr, count: int) -> sp.Expr:
    return sp.prod(value - offset for offset in range(count))


def _cycle_layout(
    lengths_by_factor: tuple[CycleMonomial, CycleMonomial, CycleMonomial]
) -> tuple[list[tuple[int, list[int]]], list[int], int]:
    cycles: list[tuple[int, list[int]]] = []
    factors: list[int] = []
    position = 0
    for factor, lengths in enumerate(lengths_by_factor):
        for length in lengths:
            positions = list(range(position, position + length))
            cycles.append((factor, positions))
            factors.extend([factor] * length)
            position += length
    return cycles, factors, position


@lru_cache(maxsize=None)
def equality_pattern_counts(
    first: CycleMonomial,
    second: CycleMonomial,
    third: CycleMonomial,
) -> tuple[tuple[tuple[int, int], int], ...]:
    """Count canonical selected-cycle assignments by generic labels and constraints."""

    cycles, factors, position_count = _cycle_layout((first, second, third))
    multipliers = (
        {},
        {0: 1, 1: 0},
        {0: 1, 1: 2, 2: 0},
    )
    counts: defaultdict[tuple[int, int], int] = defaultdict(int)
    assigned: list[int] = []
    used_by_factor = [set(), set(), set()]

    def visit(position: int, generic_count: int) -> None:
        if position == position_count:
            partial_map: dict[int, int] = {}
            inverse_map: dict[int, int] = {}
            for factor, cycle_positions in cycles:
                multiplier = multipliers[factor]
                values = [assigned[index] for index in cycle_positions]
                for index, value in enumerate(values):
                    domain = multiplier.get(value, value) if value < 3 else value
                    output = values[(index + 1) % len(values)]
                    if domain in partial_map and partial_map[domain] != output:
                        return
                    if output in inverse_map and inverse_map[output] != domain:
                        return
                    partial_map[domain] = output
                    inverse_map[output] = domain
            counts[(generic_count, len(partial_map))] += 1
            return

        factor = factors[position]
        existing_values = [*range(3), *range(3, 3 + generic_count)]
        for value in existing_values:
            if value in used_by_factor[factor]:
                continue
            assigned.append(value)
            used_by_factor[factor].add(value)
            visit(position + 1, generic_count)
            used_by_factor[factor].remove(value)
            assigned.pop()

        new_value = 3 + generic_count
        assigned.append(new_value)
        used_by_factor[factor].add(new_value)
        visit(position + 1, generic_count + 1)
        used_by_factor[factor].remove(new_value)
        assigned.pop()

    visit(0, 0)
    return tuple(sorted(counts.items()))


def selected_cycle_expectation(
    n: sp.Symbol,
    lengths: tuple[CycleMonomial, CycleMonomial, CycleMonomial],
) -> sp.Expr:
    rotation_denominator = math.prod(
        length for monomial in lengths for length in monomial
    )
    return sp.factor(
        sum(
            sp.Rational(pattern_count, rotation_denominator)
            * _falling(n - 3, generic_count)
            / _falling(n, constraint_count)
            for (generic_count, constraint_count), pattern_count in equality_pattern_counts(
                *lengths
            )
        )
    )


@lru_cache(maxsize=1)
def symbolic_shifted_character_trace() -> dict[str, object]:
    n = sp.symbols("n", integer=True, positive=True)
    correlation = sp.Integer(0)
    pattern_type_count = 0
    canonical_pattern_count = 0
    maximum_selected_points = 0
    maximum_constraint_count = 0
    for first, first_coefficient in XI_CHARACTER_TERMS:
        for second, second_coefficient in XI_CHARACTER_TERMS:
            for third, third_coefficient in W_CHARACTER_TERMS:
                lengths = (first, second, third)
                pattern_counts = equality_pattern_counts(*lengths)
                pattern_type_count += 1
                canonical_pattern_count += sum(
                    count for _, count in pattern_counts
                )
                maximum_selected_points = max(
                    maximum_selected_points, sum(map(sum, lengths))
                )
                maximum_constraint_count = max(
                    maximum_constraint_count,
                    max((key[1] for key, _ in pattern_counts), default=0),
                )
                coefficient = (
                    sp.Rational(first_coefficient.numerator, first_coefficient.denominator)
                    * sp.Rational(second_coefficient.numerator, second_coefficient.denominator)
                    * sp.Rational(third_coefficient.numerator, third_coefficient.denominator)
                )
                correlation += coefficient * selected_cycle_expectation(n, lengths)
    correlation = sp.factor(correlation)
    orbit_size = n * (n - 1) * (n - 2)
    trace = sp.factor(orbit_size * correlation)
    expected = 4 * n**3 - 46 * n**2 + 149 * n - 118
    return {
        "shifted_character_correlation": correlation,
        "orbit_size": orbit_size,
        "trace": trace,
        "expected_trace": expected,
        "identity_verified": sp.simplify(trace - expected) == 0,
        "falling_monomial_product_count": pattern_type_count,
        "canonical_equality_pattern_count": canonical_pattern_count,
        "maximum_selected_point_count": maximum_selected_points,
        "maximum_partial_permutation_constraint_count": maximum_constraint_count,
    }


def _compose(left: tuple[int, ...], right: tuple[int, ...]) -> tuple[int, ...]:
    return tuple(left[right[index]] for index in range(len(left)))


def _cycle_type(permutation: tuple[int, ...]) -> tuple[int, ...]:
    visited: set[int] = set()
    lengths: list[int] = []
    for start in range(len(permutation)):
        if start in visited:
            continue
        point = start
        length = 0
        while point not in visited:
            visited.add(point)
            length += 1
            point = permutation[point]
        lengths.append(length)
    return tuple(sorted(lengths, reverse=True))


@lru_cache(maxsize=1)
def exact_s7_endpoint_trace() -> dict[str, int | bool]:
    n = 7
    xi = (4, 2, 1)
    source = (5, 2)
    transposition = list(range(n))
    transposition[0], transposition[1] = 1, 0
    cycle = list(range(n))
    cycle[0], cycle[1], cycle[2] = 1, 2, 0
    correlation_sum = 0
    for permutation in itertools.permutations(range(n)):
        correlation_sum += (
            symmetric_character(xi, _cycle_type(permutation))
            * symmetric_character(
                xi, _cycle_type(_compose(permutation, tuple(transposition)))
            )
            * symmetric_character(
                source, _cycle_type(_compose(permutation, tuple(cycle)))
            )
        )
    orbit_size = n * (n - 1) * (n - 2)
    numerator = orbit_size * correlation_sum
    trace, remainder = divmod(numerator, math.factorial(n))
    formula_value = 4 * n**3 - 46 * n**2 + 149 * n - 118
    return {
        "n": n,
        "group_order": math.factorial(n),
        "orbit_size": orbit_size,
        "integer_character_correlation_sum": correlation_sum,
        "trace": trace,
        "division_remainder": remainder,
        "formula_value": formula_value,
        "formula_verified": remainder == 0 and trace == formula_value,
    }


