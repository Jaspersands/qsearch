"""Exact marked-cycle certificate for the stable hierarchical Racah trace.

Let W_n=(n-2,2) and xi_n=(n-3,2,1).  On the multiplicity space
Hom_Sn(V_xi, V_xi tensor V_W), the second-stage orbit Hamiltonian is the
simultaneous-conjugacy sum generated by a transposition tau=(1 2) and an
oriented 3-cycle c=(1 2 3).  Its trace is

    n(n-1)(n-2)/n! * sum_g chi_xi(g) chi_xi(g tau) chi_W(g c).

This module evaluates that shifted character correlation exactly.  Character
polynomials are expanded in falling cycle counts.  Products of selected cycles
become finite partial-permutation constraints; equality patterns among at most
eight selected points contribute (n-3)_k/(n)_r.  The stable sum simplifies to

    4 n^3 - 46 n^2 + 149 n - 118.

The equality-pattern argument is literal for n>=8.  An exact S_7 character sum
closes the endpoint.  This proves one power trace only; the remaining quartic
coefficients, root separation, coherent circuit, and decoder remain open.
"""

