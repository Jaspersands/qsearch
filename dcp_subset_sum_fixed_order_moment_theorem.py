"""General fixed-order source-moment obstruction for modular subset sum.

For each fixed factorial-moment order ``k``, ordered assignment tuples are paths
in the finite lattice-transfer system generated by the ``2^k`` Boolean column
patterns.  The all-ones target column is present from the start.  The transition
graph only enlarges lattices and is therefore acyclic after self loops are
removed.

If a terminal lattice has integer rank ``r``, its Boolean self-loop base ``b``
is at most ``2^r``: project its real span injectively onto ``r`` coordinates.
Equality forces every non-projected Boolean coordinate functional to be zero or
a copy of one projected coordinate.  Because the all-ones vector lies in the
span, zero is impossible; when ``r<k`` equality would therefore force duplicate
assignment rows.  For full rank, a source-nongeneric 2-adic lattice is proper
and cannot contain every Boolean basis vector.  Thus every bad distinct-row
state satisfies ``b/2^r <= 1-2^-k``.

The finite acyclic transfer graph then bounds every bad contribution by
``poly_k(n)*(1-2^-k)^n`` at register count ``m=n+c`` for fixed ``c``.  Hence all
fixed source factorial moments converge to their independent-map baselines.
The theorem does not cover moment order growing with n, atypical conditioned
fibers, reduced-basis geometry, or decoding complexity.
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Sequence

from research_registry import (
    ExperimentResultRecord,
    NegativeResultRecord,
    upsert_experiment_result,
    upsert_negative_result,
    utc_now,
)


DCP_SUBSET_SUM_FIXED_ORDER_MOMENT_PATH = Path(
    "research/classical_baselines/dcp_subset_sum_fixed_order_moment_theorem.json"
)
DEFAULT_EXPERIMENT_ID = "EXP-DHS-DCP-SUBSET-SUM-ALL-FIXED-MOMENT-THEOREM"
DEFAULT_CANDIDATE_ID = "DHS-GOWERS-SIEVE"


@dataclass(frozen=True)
class FixedOrderMomentCertificate:
    moment_order: int
    boolean_column_pattern_count: int
    finite_lattice_state_upper_bound: str
    bad_growth_ratio_upper_bound_numerator: int
    bad_growth_ratio_upper_bound_denominator: int
    bad_growth_ratio_upper_bound: float
    coordinate_projection_bound_proved: bool
    equality_forces_coordinate_copies_proved: bool
    distinct_rows_force_strict_rank_deficient_contraction: bool
    proper_full_rank_two_adic_lattice_forces_strict_contraction: bool
    finite_transfer_graph_proved_monotone: bool
    fixed_offset_source_excess_vanishes: bool
    asymptotic_excess_bound: str
    proof: str
    limitations: list[str]


@dataclass(frozen=True)
class DCPSubsetSumFixedOrderMomentReport:
    created_at: str
    theorem_contract: dict[str, str]
    certificates: list[FixedOrderMomentCertificate]
    headline_metrics: dict[str, int | float]
    claim_gate: dict[str, bool | str]
    status: str
    summary: str
    falsifiers_triggered: list[str]


def fixed_order_moment_certificate(moment_order: int) -> FixedOrderMomentCertificate:
    if moment_order < 2:
        raise ValueError("moment_order must be at least two")
    denominator = 1 << moment_order
    numerator = denominator - 1
    return FixedOrderMomentCertificate(
        moment_order=moment_order,
        boolean_column_pattern_count=denominator,
        finite_lattice_state_upper_bound=f"2^(2^{moment_order})",
        bad_growth_ratio_upper_bound_numerator=numerator,
        bad_growth_ratio_upper_bound_denominator=denominator,
        bad_growth_ratio_upper_bound=numerator / denominator,
        coordinate_projection_bound_proved=True,
        equality_forces_coordinate_copies_proved=True,
        distinct_rows_force_strict_rank_deficient_contraction=True,
        proper_full_rank_two_adic_lattice_forces_strict_contraction=True,
        finite_transfer_graph_proved_monotone=True,
        fixed_offset_source_excess_vanishes=True,
        asymptotic_excess_bound=f"poly_{moment_order}(n)*(1-2^-{moment_order})^n",
        proof=(
            "Choose r coordinates giving an injective projection of the rank-r real span. This injects its Boolean "
            "points into {0,1}^r. Equality makes each remaining linear coordinate functional Boolean on the full cube; "
            "evaluation on e_i and e_i+e_j proves it is zero or one coordinate copy. The all-ones target excludes zero, "
            "and distinct rows exclude copies when r<k. At full rank, any lattice with an even Smith factor is proper "
            "and omits a Boolean basis vector. Thus b<=2^r-1 for every source-nongeneric terminal state. Finite monotone "
            "lattice transfer converts this strict ratio into the stated exponential bound."
        ),
        limitations=[
            "Moment order must remain fixed while n grows.",
            "The finite-state and polynomial constants can be enormous functions of the order.",
            "The theorem is source averaged over uniform labels and independent target.",
            "It does not prove concentration for a selected low-bit fiber.",
            "It supplies no implicit statistic, witness decoder, or computational lower bound.",
        ],
    )


def run_fixed_order_moment_theorem(
    moment_orders: Sequence[int] = tuple(range(2, 13)),
) -> DCPSubsetSumFixedOrderMomentReport:
    certificates = [fixed_order_moment_certificate(order) for order in moment_orders]
    if len({item.moment_order for item in certificates}) != len(certificates):
        raise ValueError("moment orders must be unique")
    proved = sum(item.fixed_offset_source_excess_vanishes for item in certificates)
    metrics: dict[str, int | float] = {
        "certificate_count": len(certificates),
        "proved_fixed_order_source_obstruction_count": proved,
        "general_all_fixed_orders_theorem_count": int(bool(certificates) and proved == len(certificates)),
        "largest_instantiated_fixed_order": max((item.moment_order for item in certificates), default=0),
        "maximum_instantiated_bad_growth_ratio_upper_bound": max(
            (item.bad_growth_ratio_upper_bound for item in certificates), default=0.0
        ),
        "proved_growing_order_obstruction_count": 0,
        "proved_atypical_conditioned_fiber_obstruction_count": 0,
        "polynomial_witness_decoder_count": 0,
    }
    return DCPSubsetSumFixedOrderMomentReport(
        created_at=utc_now(),
        theorem_contract={
            "source": "uniform independent labels and uniform independent target over Z_(2^n)",
            "regime": "m=n+c with fixed c; factorial moment order k fixed before n tends to infinity",
            "conclusion": "E[(C)_k]-(2^m)_k/2^(nk) tends to zero exponentially up to a k-dependent polynomial",
            "excluded": "k=k(n), conditioned-fiber tails, basis geometry, and decoder complexity",
        },
        certificates=certificates,
        headline_metrics=metrics,
        claim_gate={
            "all_fixed_source_moment_orders_closed": True,
            "growing_order_closed": False,
            "atypical_conditioned_fibers_closed": False,
            "reduced_basis_geometry_closed": False,
            "polynomial_witness_decoder_constructed": False,
            "speedup_claim_allowed": False,
            "reason": (
                "Every fixed-order source-average factorial-moment excess contracts. Only an order growing with n, "
                "an atypical conditioned-fiber theorem, or a non-moment geometric mechanism can evade this gate."
            ),
        },
        status="all-fixed-source-moment-orders-asymptotically-obstructed",
        summary=(
            f"Instantiated {len(certificates)} fixed-order certificates through k="
            f"{metrics['largest_instantiated_fixed_order']}. The projection/copy lemma proves the same strict "
            "contraction for every fixed k; growing order and conditioned-fiber concentration remain open."
        ),
        falsifiers_triggered=[
            "No generic source-average statistic of any fixed factorial-moment order can retain asymptotic excess.",
            "Increasing a fixed constant moment order does not evade the theorem.",
            "Finite-state constants depending badly on k prevent applying the result to k growing with n.",
            "Source-average decay alone does not rule out a proved inverse-polynomial atypical conditioned-fiber tail.",
        ],
    )


def write_fixed_order_moment_theorem(
    path: Path = DCP_SUBSET_SUM_FIXED_ORDER_MOMENT_PATH,
    moment_orders: Sequence[int] = tuple(range(2, 13)),
    write_registry: bool = True,
    registry_experiment_id: str = DEFAULT_EXPERIMENT_ID,
    registry_candidate_id: str = DEFAULT_CANDIDATE_ID,
    registry_result_id: str | None = None,
) -> dict:
    report = run_fixed_order_moment_theorem(moment_orders=moment_orders)
    payload = asdict(report)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=True))
    if write_registry:
        upsert_negative_result(
            NegativeResultRecord(
                id="NEG-DCP-SUBSET-SUM-ALL-FIXED-SOURCE-MOMENTS",
                source=str(path),
                claim=(
                    "Some sufficiently large but fixed source factorial-moment order generically retains a persistent "
                    "density-one modular subset-sum signal."
                ),
                reason_invalid=(
                    "For every fixed order k, finite monotone lattice transfer and the Boolean-subspace projection "
                    "lemma bound every bad state's relative growth by at most 1-2^-k."
                ),
                lesson=(
                    "Stop escalating fixed moment degree. Search only growing-order mechanisms with charged resources, "
                    "proved atypical conditioned fibers, or non-moment reduced-basis geometry."
                ),
                applies_to=[registry_candidate_id, registry_experiment_id],
                evidence=payload["headline_metrics"],
            )
        )
        result_id = registry_result_id or f"RESULT-{registry_experiment_id}-ALL-FIXED-MOMENT-THEOREM"
        upsert_experiment_result(
            ExperimentResultRecord(
                id=result_id,
                experiment_id=registry_experiment_id,
                candidate_id=registry_candidate_id,
                created_at=payload["created_at"],
                status=payload["status"],
                summary=payload["summary"],
                metrics=payload["headline_metrics"],
                falsifiers_triggered=payload["falsifiers_triggered"],
                artifacts={"dcp_subset_sum_fixed_order_moment_theorem": str(path)},
            )
        )
    return payload
