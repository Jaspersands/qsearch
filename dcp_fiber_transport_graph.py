"""Exact small-instance graph audit for 2-adic subset-sum fiber transport.

Vertices are assignments in a low-bit fiber F_k(t).  Edges are generated by
the explicit local transports certified in dcp_two_adic_fiber_transport:
divisible-coordinate flips, equal-low-residue coordinate swaps, and fixed
small-block pattern substitutions with equal low-bit sums.

The audit measures component fragmentation, child-bit mixing, and the
normalized-adjacency spectral gap on the largest component.  The same graph is
given to a classical BFS baseline.  All calculations are finite and exact up
to the floating-point eigenvalue computation; no finite gap is promoted to a
uniform polynomial-gap theorem.
"""

